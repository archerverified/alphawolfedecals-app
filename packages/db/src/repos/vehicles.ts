export async function listYears(): Promise<number[]> {
  return withSystem(async (db) => {
    const rows = await db.vehicle.findMany({
      where: { status: 'published' },
      distinct: ['year'],
      select: { year: true },
      orderBy: { year: 'desc' },
    });
    return rows.map((r) => r.year);
  });
}

