// Vehicle template repository (GH-003 / GH-004).
//
// Two access shapes, matching the RLS model (see prisma/sql/auth_rls.sql):
//   * Public browse/search/detail run on withSystem() — vehicle templates are
//     shared, public, NON-PII catalog data with no per-user scoping. withSystem
//     bypasses RLS, so EVERY public function here MUST filter status='published'
//     itself; the published-only guarantee for that path lives in these queries,
//     not in RLS.
//   * Admin CRUD runs on withUser(adminId) — RLS enforces admin-only writes via
//     app_is_admin(), and lets the admin read every status.
//
// Never expose the Prisma client. Callers get plain row objects.

