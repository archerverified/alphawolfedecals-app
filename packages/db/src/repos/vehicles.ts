export async function findPublished(filter: CascadeFilter): Promise<VehicleSummary[]> {
  return withSystem(async (db) => {
    return db.vehicle.findMany({
      where: buildPublishedWhere(filter),
      select: SUMMARY_SELECT,
      orderBy: [{ year: 'desc' }, { make: 'asc' }, { model: 'asc' }, { trim: 'asc' }],
      take: 100,
    });
  });
}

export async function searchPublished(query: string): Promise<VehicleSummary[]> {
  const sql = buildVehicleSearchSql(query);
  if (!sql) return [];
  return withSystem(async (db) => {
    // Loosen the trigram threshold for short/typo queries (default 0.3). Local
    // to this transaction so it can't leak across pool reuse.
    await db.$executeRawUnsafe(`SELECT set_config('pg_trgm.similarity_threshold', '0.2', true)`);
    const rows = await db.$queryRawUnsafe<Array<VehicleSummary & { sim: number }>>(sql);
    return rows.map(({ sim: _sim, ...v }) => v);
  });
}

export async function getPublishedDetail(id: string): Promise<VehicleDetail | null> {
  return withSystem(async (db) => {
    const v = await db.vehicle.findFirst({
      where: { id, status: 'published' },
      include: { panels: { orderBy: { installOrder: 'asc' } } },
    });
    return v ? toDetail(v) : null;
  });
}

// Curated Alpha Wolf catalogue for the /vehicles browse grid (Goal 2a). Returns
// only published rows that carry an AW-TPL id, newest-catalogued first
// (alpha_wolf_tpl_id ascending keeps AW-TPL-0001..0003 in a stable order). Runs
// on withSystem (public, non-PII catalogue data) — so it filters status itself.
export async function listAlphaWolfTemplates(): Promise<AlphaWolfTemplateCard[]> {
  return withSystem(async (db) => {
    const rows = await db.vehicle.findMany({
      where: { status: 'published', alphaWolfTplId: { not: null } },
      select: {
        id: true,
        year: true,
        make: true,
        model: true,
        trim: true,
        bodyType: true,
        thumbPngUrl: true,
        viewCount: true,
        scaleDenom: true,
        dimensionsText: true,
        alphaWolfTplId: true,
      },
      orderBy: { alphaWolfTplId: 'asc' },
    });
    // alphaWolfTplId is non-null by the where filter; narrow the type for callers.
    return rows.map((r) => ({ ...r, alphaWolfTplId: r.alphaWolfTplId as string }));
  });
}

