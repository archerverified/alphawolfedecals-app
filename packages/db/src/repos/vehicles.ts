// Translate a cascade/facet filter into a Prisma where clause, always pinned to
// published rows. Empty/undefined fields are omitted so each cascade level
// widens correctly. trim is tri-state: `null` means "base trim (no trim)",
// undefined means "any trim".
export function buildPublishedWhere(filter: CascadeFilter): Prisma.VehicleWhereInput {
  const where: Prisma.VehicleWhereInput = { status: 'published' };
  if (filter.year !== undefined) where.year = filter.year;
  if (filter.make) where.make = filter.make;
  if (filter.model) where.model = filter.model;
  if (filter.trim !== undefined) where.trim = filter.trim;
  if (filter.bodyType) where.bodyType = filter.bodyType;
  if (filter.cabSize) where.cabSize = filter.cabSize;
  if (filter.bedSize) where.bedSize = filter.bedSize;
  if (filter.roofHeight) where.roofHeight = filter.roofHeight;
  return where;
}

// The searchable text expression — kept byte-identical to the tsvector and
// trigram indexes in the migration so the planner can use them.
const SEARCH_EXPR =
  "coalesce(make,'') || ' ' || coalesce(model,'') || ' ' || " +
  "coalesce(trim,'') || ' ' || coalesce(variant,'') || ' ' || year::text";

// Build the typo-tolerant search SQL. Combines a pg_trgm similarity match (for
// typos: "transt 250" -> "Transit 250") with an AND of per-term ILIKE
// substring matches (for precise multi-field queries: "2024 transit 250").
// Values are escaped via pgQuoteLiteral — the only safe raw-SQL shape on the
// transaction pooler (see client.ts). Returns null when the query is blank.
export function buildVehicleSearchSql(query: string, limit = 50): string | null {
  const q = query.trim();
  if (!q) return null;
  const terms = q.split(/\s+/).filter(Boolean).slice(0, 8);
  const trigram = `(${SEARCH_EXPR}) % ${pgQuoteLiteral(q)}`;
  const termIlikes = terms
    .map((t) => `(${SEARCH_EXPR}) ILIKE ${pgQuoteLiteral(`%${t}%`)}`)
    .join(' AND ');
  const lim = Math.max(1, Math.min(200, Math.trunc(limit)));
  return `
    SELECT id, year, make, model, trim, variant, body_type AS "bodyType",
           length_mm AS "lengthMm", width_mm AS "widthMm", height_mm AS "heightMm",
           outline_svg_url AS "outlineSvgUrl", thumb_png_url AS "thumbPngUrl",
           status, version,
           similarity((${SEARCH_EXPR}), ${pgQuoteLiteral(q)}) AS sim
    FROM vehicles
    WHERE status = 'published'
      AND ( ${trigram} OR (${termIlikes}) )
    ORDER BY sim DESC, year DESC, make ASC, model ASC
    LIMIT ${lim}`;
}

// Reduce a set of published rows to the distinct facet values present, so the UI
// only ever offers facets that resolve to a real template. Pure — unit-tested.
export function summariseFacets(
  rows: Array<{
    bodyType: BodyType;
    cabSize: string | null;
    bedSize: string | null;
    roofHeight: string | null;
  }>,
): VehicleFacets {
  const uniq = <T>(xs: Array<T | null | undefined>): T[] =>
    [...new Set(xs.filter((x): x is T => x != null))].sort();
  return {
    bodyTypes: uniq(rows.map((r) => r.bodyType)),
    cabSizes: uniq(rows.map((r) => r.cabSize)),
    bedSizes: uniq(rows.map((r) => r.bedSize)),
    roofHeights: uniq(rows.map((r) => r.roofHeight)),
  };
}

